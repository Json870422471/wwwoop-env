class Translator(QObject):
    language_changed = pyqtSignal()

    def __init__(self, parent=None):
        super().__init__(parent)
        self._lang = LANG_ZH

    @property
    def lang(self) -> str:
        return self._lang

    def toggle(self):
        self._lang = LANG_EN if self._lang == LANG_ZH else LANG_ZH
        self.language_changed.emit()

    def t(self, key: str, **kwargs) -> str:
        text = _TRANSLATIONS.get(self._lang, _TRANSLATIONS[LANG_ZH]).get(key, key)
        if kwargs:
            try:
                return text.format(**kwargs)
            except (KeyError, IndexError):
                return text
        return text

    @property
    def switch_label(self) -> str:
        return _TRANSLATIONS[self._lang]["lang.switch"]


_translator_instance: Translator | None = None


def get_translator() -> Translator:
    global _translator_instance
    if _translator_instance is None:
        _translator_instance = Translator()
    return _translator_instance
